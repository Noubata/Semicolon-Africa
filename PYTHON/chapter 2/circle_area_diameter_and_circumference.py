#2.5 (Circle Area, Diameter and Circumference) For a circle of radius 2, display the diameter, circumference and area. Use the value 3.14159 for π. Use the following formulas (r is the radius): diameter = 2r, circumference = 2πr and area = πr2. 
#Solution

radius = 2
pi_value = 3.14159

diameter = 2*radius
print('the diameter is', diameter)

circumference = 2*pi_value*radius
print('the circumference is', circumference)

area = pi_value*radius*2
print('the area is', area)